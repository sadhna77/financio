const mongoose = require('mongoose');

// Create User schema
const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  profilePic: {
    type: String,
    default: ""
  }
});

module.exports = mongoose.model('UserFinancio', UserSchema);
